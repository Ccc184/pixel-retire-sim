export function buildEndingText(endingId: string, choices: OriginChoices): string {
  const ending = ENDINGS.find(e => e.id === endingId);
  if (!ending) return '未知结局。';
  
  const cityQuote = CITY_REASON_QUOTES[choices.cityReason];
  const careerQuote = CAREER_MOTIVATION_QUOTES[choices.careerMotivation];
  const riskQuote = RISK_ATTITUDE_QUOTES[choices.riskAttitude];
  
  // 按设计书格式：第一段结局引入 + [城市回响] + [驱动力回响] + [风险回响] + 结局主体
  // 设计书中每个结局的结构是：开头段落 -> [城市回响] -> 中间段落 -> [驱动力回响] -> [风险回响] -> 结尾段落
  // 但设计书只预留了[城市回响]一个占位符。我们按照伪代码逻辑，将三个回响依次插入。
  // 为符合设计书的文学结构，采用：结局正文 -> 空行 -> 城市回响 -> 职业驱动力回响 -> 风险态度回响
  
  // 取结局开头到第一个空行（引入段）
  const paragraphs = ending.skeleton.split('\n\n');
  const intro = paragraphs[0];
  const body = paragraphs.slice(1).join('\n\n');
  
  return `${intro}

${cityQuote}

${careerQuote}

${riskQuote}

${body}`;
}

